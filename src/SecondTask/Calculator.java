package SecondTask;

public class Calculator {
    public double sum(double numb, double numb2) {
        return numb + numb2;
    }
    public double min(double numb, double numb2) {
        return numb - numb2;
    }
    public double div(double numb, double numb2) {
        return  numb / numb2;
    }
    public double multiplication(double numb, double numb2) {
        return numb * numb2;
    }
    public double Math(double numb, double numb2) {
        return Math.pow(numb, numb2);
    }
}
